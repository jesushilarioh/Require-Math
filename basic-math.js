// Basic Math
module.exports.Add = function (x, y){
  return x + y;
};
module.exports.Divide = function (x, y) {
  return x / y;
};
module.exports.Modulus = function (x, y) {
  return x % y;
};
module.exports.Multiply = function (x, y) {
  return x * y;
};
module.exports.Subtract = function (x, y) {
  return x - y;
};
